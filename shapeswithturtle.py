# x.hideturtle()-  it hides the pointer!
# Forward() - Defines the distance to be moved in the forward direction.
# Backward() - Defines the distance to be moved in the backward direction!
# Left() - Defines the angle with which we have to bent our pointer in left direction.
# Right() - Defines the angle with which we have to bent our pointer in right direction.

